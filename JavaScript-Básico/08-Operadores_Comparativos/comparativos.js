// == Igual a
// === Valor igual e igual
// != Diferente
// !== Não igual ou não igual
// > Maior que
// < Menor que
// >= Maior ou igual a
// <= Menor ou igual a

var a = 3;
var b = "3";
console.log(a == b);
console.log(a === b);
console.log(a != b);
console.log(a !== b);

